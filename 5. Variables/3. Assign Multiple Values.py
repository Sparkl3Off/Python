# Asignación de múltiples valores a múltiples variables en una línea
x, y, z = "apple", "banana", "pear"
print(x)
print(y)
print(z)

# Asignación de un valor a múltiples variables en una línea
a = b = c = "cherry"
print(a)
print(b)
print(c)

# Asignación de múltiplels valores dentro de una lista a múltiples variables en dos líneas
