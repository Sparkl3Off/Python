# Se puede modificar el String a Mayúscula
a = "Hello, World!" # Se asigna el texto a una variable
print(a.upper()) # Imprime el texto en mayúscula

# Se puede modificar el String a Minúscula
a = "Hello, World!" # Se asigna el texto a una variable
print(a.lower()) # Imprime el texto en minúscula

# Se puede modificar el String con espacios para que no los tenga
a = "   Hello, World!    " # Se asigna el texto a una variable
print(a.strip()) # Imprime el texto sin espacios innecesarios

# Se puede modificar una letra dentro del String
a = "Hello, World!" # Se asigna el texto a una variable
print(a.replace("H", "J")) # Imprime el texto con la letra específica cambiada

# Se puede modificar el String para separar el texto con los caractéres de la cadena
a = "Hello, World!" # Se asigna el texto a una variable
print(a.split("o")) # Imprime el texto con la cadena especifica como separador